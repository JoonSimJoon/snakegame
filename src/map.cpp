Map::Map(int width, int height, int map_index)
    : width(width), height(height), map_index(map_index) {
    generate();
}

void Map::generate() {
    mapData = std::vector<std::vector<int>>(height, std::vector<int>(width, ' '));
    for (int x = 0; x < height; x++) {
        for (int y = 0; y < width; y++) {
            mapData[x][y] = map_txt[map_index][x][y];
        }
    }
}

const std::vector<std::vector<int>>& Map::getMapData() const {
    return mapData;
}



